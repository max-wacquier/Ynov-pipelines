def addCalc(a, b):
    """
        paramètre a : (int) un entier
        paramètre b : (int) un entier
        valeur renvoyée : (int) la somme de a et b.

    Exemples :

    >>> addCalc(3, 4)
    7

    >>> addCalc(10, 5)
    15

    >>> addCalc(-10, 5)
    -5

    """
    return (int(a) + int(b))